import Foundation

/*
 
 Objective-C与Swift：
 - swift注重面向协议编程、函数式编程、面向对象编程，OC注重面向对象编程
 
 - swift注重值类型，OC注重指针和引用
   - 值类型的数据存储在内存的栈中，引用类型的数据存储在内存的堆中
   - 值类型的变量直接存放实际的数据，而引用类型的变量存放的则是数据的地址，即对象的引用
 
 - swift是静态类型语言，OC是动态类型语言 (id)
 
 */



